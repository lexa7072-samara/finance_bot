    def detect_category(self, text: str) -> str:
        text_lower = text.lower()
        
        for category, keywords in self.category_keywords.items():
            for keyword in keywords:
                if keyword in text_lower:
                    return category
        
        return 'Другое'
    
    def detect_income_category(self, text: str) -> str:
        text_lower = text.lower()
        
        for category, keywords in self.income_keywords.items():
            for keyword in keywords:
                if keyword in text_lower:
                    return category
        
        return 'Другой доход'
    
    def parse_expense(self, text: str) -> Tuple[float, str, bool]:
        parts = text.strip().split(maxsplit=1)
        
        if not parts:
            raise ValueError("Пустое сообщение")
        
        amount_str = parts[0]
        is_income = amount_str.startswith('+')
        
        if is_income:
            amount_str = amount_str[1:]
        
        try:
            amount = float(amount_str.replace(',', '.'))
        except ValueError:
            raise ValueError("Не удалось распознать сумму. Формат: 500 расход или +5000 доход")
        
        description = parts[1] if len(parts) > 1 else ''
        
        return amount, description, is_income
    
    def get_funny_comment(self, category: str) -> str:
        comments = self.funny_comments.get(category, self.funny_comments['Другое'])
        return random.choice(comments)
    
    def get_income_comment(self, category: str) -> str:
        comments = self.income_comments.get(category, self.income_comments['Другой доход'])
        return random.choice(comments)